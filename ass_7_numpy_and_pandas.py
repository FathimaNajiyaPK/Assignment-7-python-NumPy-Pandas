#  Exercise 4: Create a DataFrame with 10 rows( with the following columns: name, age, and gender. The dataframe should have 10 rows of data)

data = {
    'name': ['Alice', 'Bob', 'Charlie', 'David', 'Eva', 'Frank', 'Grace', 'Hannah', 'Ian', 'Julia'],
    'age': [25, 30, 22, 35, 29, 40, 28, 32, 26, 31],
    'gender': ['F', 'M', 'M', 'M', 'F', 'M', 'F', 'F', 'M', 'F']
}
df = pd.DataFrame(data)
print("Exercise 4: DataFrame with 10 rows:")
print(df)
print()

#  Exercise 5: Add a new column 'occupation'

occupations = ['Programmer', 'Manager', 'Analyst', 'Programmer', 'Manager',
               'Analyst', 'Programmer', 'Manager', 'Analyst', 'Programmer']
df['occupation'] = occupations
print("Exercise 5: DataFrame with 'occupation' column added:")
print(df)
print()

#  Exercise 6: Select rows where age >= 30

age_filtered = df[df['age'] >= 30]
